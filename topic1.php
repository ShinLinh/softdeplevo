<h2 class="head"><a id="history">History of the evolution of the World Wide Web</a></h2>
<article>
<p>To understand what <strong>Web 3.0</strong> is, first, we must understand what its predecessors, <strong>Web 1.0</strong> and <strong>Web 2.0</strong> are.</p>
<p> <strong>Web 1.0</strong> is only a term to refer to the first generation of the World Wide Web, where most Internet users are only consumers, and there were very few content creators. 
During this generation, the main focus was to build the Web and make it accessible to consumers. Experts call <strong>Web 1.0</strong> static, or "read-only" Web, as 
there was no active information flow between the consumers and the producers, the web only allowed users to search and read information. The need for interaction between users and creators 
led to the advance to the next generation of the Web, <strong>Web 2.0</strong>, which we are currently using.</p>
<blockquote cite="http://darcyd.com/fragmented_future.pdf">
<p>The Web we know now, which loads into a browser window in essentially static screenfuls, is only an embryo of the Web to come. The first glimmerings of Web 2.0 are beginning to appear, 
and we are just starting to see how that embryo might develop. The Web will be understood not as screenfuls of text and graphics but as a transport mechanism, the ether through which 
interactivity happens. It will [...] appear on your computer screen, [...] on your TV set [...] your car dashboard [...] your cell phone [...] hand-held game machines [...] maybe even 
your microwave oven.</p>
<p> - Darcy DiNucci, 1999, "Fragmented Future".  </p>
</blockquote>
<p><strong>Web 2.0</strong>, which is described by Tim Berners-Lee, the creator of the World Wide Web as a jargon, is the opposite of <strong>Web 1.0</strong>. The term was first mentioned in 1999 by
Darcy DiNucci, but later became popular after they were mentioned at the O'Reilly Media Web 2.0 Conference by Tim O'Reilly and Dale Dougherty. In the <strong>Web 2.0</strong> era, every user
can interact with the Web, and contribute content to it via different methods and services, such as video/image sharing websites(Youtube, imgur, instagram), social networks(Facebook, Twitter, Google+),
or information websites(wikipedia),... And in the recent years, with the development of new technologies, as well as the drastic increase in the number of devices that can have
access to the internet, the World Wide Web is advancing to its next stage of the evolution, Web 3.0. 
</p>
</article>
<h2 class="head"><a id="web3">Web 3.0</a></h2>
<article>
<p>
<img id="illustration" src="images/Web3.jpg" alt="Web3.0" width="500" height="271"/>
The term <strong>Web 3.0</strong> was coined by John Markoff of the New York Times in 2006. There are many debates on what its real definition is, or rather, is going to be,
but the most common idea is that <strong>Web 3.0</strong> can be called an "intelligent Web". Web applications services and will be able to communicate between each other, as well as
understand context of data input, and as a result, will be able to find out other relevant information, helping users complete certain tasks more efficiently than <strong>Web 2.0</strong>. This video below
by Youtube user <em>Alejandro Saucedo</em> will help you understand the basic things about <strong>Web 3.0</strong>.
</p>
<iframe width="560" height="315" src="https://www.youtube.com/embed/F_nbUizGeEY" allowfullscreen="allowfullscreen" id="video"></iframe>
<p> 
There are also several other characteristics of <strong>Web 3.0</strong> that have been defined by experts:</p>
<ul class="char">
	<li>Use of Semantic Web technologies</li>
	<li>Universal connectivity between all types of Internet devices available at any time, any place</li>
	<li>Open API, data formats and protocols</li>
	<li>...</li>
</ul> 
